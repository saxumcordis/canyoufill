#include "libft.h"

int			ft_sqrt(int nb)
{
	int i;

	if (nb % 10 == 2 || nb % 10 == 3 || nb % 10 == 7 || nb % 10 == 8
		|| nb == 0 || nb < 0)
		return (0);
	i = 1;
	while (i < nb / 2)
	{
		if (i == 46341)
			return (0);
		if (i * i == nb)
			return (i);
		++i;
	}
	return (i);
}
